import { Comment } from '../../domain/post/entities/Comment';

/**
 * CommentFormatter Service
 * SOLID: Single Responsibility - Format comment for display
 */
export class CommentFormatter {
  static formatTime(dateString: string): string {
    const date = new Date(dateString);
    const now = new Date();
    const diffMs = now.getTime() - date.getTime();
    const diffMins = Math.floor(diffMs / 60000);
    const diffHours = Math.floor(diffMs / 3600000);
    const diffDays = Math.floor(diffMs / 86400000);

    if (diffMins < 1) return 'Just now';
    if (diffMins < 60) return `${diffMins}m`;
    if (diffHours < 24) return `${diffHours}h`;
    if (diffDays < 7) return `${diffDays}d`;

    return date.toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
    });
  }

  static getInitials(username: string): string {
    return username.charAt(0).toUpperCase();
  }
}
